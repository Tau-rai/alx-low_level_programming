#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - adds two integers
 * @a: an int
 * @b: an int
 * Return: Always 0.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two ints
 * @a: an int
 * @b: an int
 * Return: Always 0.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 * @a: an int
 * @b: an int
 * Return: Always 0.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 * @a: an int
 * @b: an int
 * Return: Always 0.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of two ints
 * @a: an int
 * @b: an int
 * Return: Always 0.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
